int		ft_sqrt(int nb)
{
	double	i;

	i = 0;
	if (nb > 4000000)
		i = 2000;
	while ((i * i) <= nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
